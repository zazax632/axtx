function createFixedFallingText(layerId, texts, speed = 1) {
  const layer = document.getElementById(layerId);
  if (!layer) return;
  layer.innerHTML = "";

  texts.forEach((text) => {

    for (let i = 0; i < 3; i++) {
      
      const span = document.createElement("span");
      span.textContent = text;
      span.style.left = Math.random() * (window.innerWidth - 200) + "px";
      span.style.top = Math.random() * (window.innerHeight - 20) + "px";
      span.style.fontSize = Math.random() * 18 + 12 + "px";
      span.style.opacity = Math.random() * 0.6 + 0.4;
      layer.appendChild(span);

      let topPos = parseFloat(span.style.top);
      setInterval(() => {
        topPos += speed;
        if (topPos > window.innerHeight) topPos = -20;
        span.style.top = topPos + "px";
      }, 50);
    }
  });
}


